    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here
        String id = txtID.getText().trim();
        int check = 0;
        if (id.equals("")) {
            JOptionPane.showMessageDialog(this, "Please input ID");
        } else {
            for (EmpDTO empDTO : list) {
                if (id.equalsIgnoreCase(empDTO.getId()) && empDTO.getIsDelete() == false) {
                    txtID.setText(empDTO.getId());
                    txtName.setText(empDTO.getName());
                    txtPhone.setText(empDTO.getPhone());
                    txtEmail.setText(empDTO.getEmail());
                    txtAddress.setText(empDTO.getAddress());
                    txtDOB.setText(empDTO.getDob());
                    check++;
                }
            }
            if(check == 0){
                JOptionPane.showMessageDialog(this, "NOT FOUND");
            }
        }

    }//GEN-LAST:event_btnFindActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String valid = "";
        String id = txtID.getText().trim();
        for (EmpDTO empDTO : list) {
            if (id.equals(empDTO.getId())) {
                valid += "ID duplicated \n";
            }
        }
        String name = txtName.getText().trim();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText().trim();
        String dob = txtDOB.getText();
        EmpDTO dto = new EmpDTO(id, name, phone, email, address, dob);
        valid  += checkValid(dto);
        if (valid.equals("")) {
            list.add(dto);
            JOptionPane.showMessageDialog(this, "Create success");
            clear();
        } else {
            JOptionPane.showMessageDialog(this, valid);
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(pos != -1){
            String id = txtID.getText().trim();
            String name = txtName.getText().trim();
            String phone = txtPhone.getText().trim();
            String email =  txtEmail.getText().trim();
            String address = txtAddress.getText().trim();
            String dob = txtDOB.getText().trim();
            EmpDTO dto = new EmpDTO(id, name, phone, email, address, dob);
            String valid = checkValid(dto);
            if(valid.equals("")){
                list.get(pos).setId(id);
                list.get(pos).setName(name);
                list.get(pos).setPhone(phone);
                list.get(pos).setEmail(email);
                list.get(pos).setAddress(address);
                list.get(pos).setDob(dob);
                JOptionPane.showMessageDialog(this, "Update success");
                clear();
                loadEmp();
            }
            else{
                JOptionPane.showMessageDialog(this, "Update Fail");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Choose a employee before you update");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

