    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        if (pos != -1) {
            int option = JOptionPane.showConfirmDialog(this, "Sure Remove?", "REMOVE", JOptionPane.YES_NO_OPTION);
            if (option == 0) {
                list.get(pos).setIsDelete(true);
                JOptionPane.showMessageDialog(this, "Remove success");
                loadEmp();
                clear();
                pos = -1;
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Please choose before you remove");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

