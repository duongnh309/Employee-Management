    public String checkValid(EmpDTO dto) {
        String valid = "";
        String id = dto.getId();
        boolean checkId = util.isIDValid(id);
        if (checkId == false) {
            valid += "Invalid ID \n";
        }

        String name = dto.getName();

        String phone = dto.getPhone();
        boolean checkPhone = util.isPhoneValid(phone);
        if (checkPhone == false) {
            valid += "Invalid phone number \n";
        }

        String email = dto.getEmail();
        boolean checkEmail = util.isEmailValid(email);
        if (checkEmail == false) {
            valid += "Invalid Email \n";
        }

        String address = dto.getAddress();

        String dob = dto.getDob();
        boolean checkDOB = util.isDateValid(dob);

        if (checkDOB == false) {
            valid += "Invalid DateOfBirth \n";
        }
        if (name.isEmpty()) {
            valid += "Name can't be empty \n";
        }
        if (email.isEmpty()) {
            valid += "Email can't be empty \n";
        }
        if (address.isEmpty()) {
            valid += "Address can't be empty \n";
        }
        if (id.isEmpty()) {
            valid += "ID can't be empty \n";
        }
        if (name.length() > 30) {
            valid += "Name must be less than 30 \n";
        }
        if (address.length() > 300) {
            valid += "Address must be less than 300 \n";
        }
        if (email.length() > 30) {
            valid += "Email must be less than 30 \n";
        }
        return valid;
    }

